type Params = Record<string, string | number>

function interpolate(message: string, params?: Params): string {
  if (!params) return message
  return message.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`))
}

interface I18nValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (source: string, params?: Params) => string
}

const I18nContext = createContext<I18nValue>({
  locale: "zh-CN",
  setLocale: () => undefined,
  t: (source, params) => interpolate(zhCN[source] ?? source, params),
})

export function I18nProvider({ children }: { children: ReactNode }) {
  const { preferences, updatePreferences } = useBrowserPreferences()
  const locale = preferences.locale

  const setLocale = useCallback(
    (next: Locale) => {
      updatePreferences((current) => ({ ...current, locale: next }))
    },
    [updatePreferences],
  )

  const t = useCallback(
    (source: string, params?: Params) => {
      const message = locale === "zh-CN" ? (zhCN[source] ?? source) : source
      return interpolate(message, params)
    },
    [locale],
  )

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nValue {
  return useContext(I18nContext)
}
